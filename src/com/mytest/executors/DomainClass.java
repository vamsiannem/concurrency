package com.mytest.executors;

public class DomainClass {

	/**
	 * @param name
	 */
	public DomainClass(String name) {
		super();
		this.name = name;
	}

	private String name;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
